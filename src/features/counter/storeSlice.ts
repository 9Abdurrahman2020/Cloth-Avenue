import { createSlice } from '@reduxjs/toolkit';
import { ICart, initialProduct, InitialState, IProduct, ISearch, TMenSelectedCategory, TStatus, TWomenSelectedCategory } from '../Types';

const initialState: InitialState = {
  products: [],
  status: 'idle',
  womenSelectedCategory: "tanks",
  women: [],
  cart: [],
  menSelectedCategory: "t-shirt",
  men: [],
  singleProduct: initialProduct,
  categoryBasedProducts: [],
  cartPrice: 0,
  filter: {},
  navHeight: 0
}

// The function below is called a thunk and allows us to perform async logic. It
// can be dispatched like a regular action: `dispatch(incrementAsync(10))`. This
// will call the thunk with the `dispatch` function as the first argument. Async
// code can then be executed and other actions can be dispatched. Thunks are
// typically used to make async requests.

export const storeSlice = createSlice({
  name: 'clothStore',
  initialState,
  // The `reducers` field lets us define reducers and generate associated actions
  reducers: {
    setWomenSelectedCategory: (state, action:{type:string, payload: TWomenSelectedCategory}) =>{
      state.womenSelectedCategory = action.payload;
    },
    setMenSelectedCategory: (state, action:{type:string, payload: TMenSelectedCategory}) =>{
      state.menSelectedCategory = action.payload;
    },
    setWomenProduct: (state) => {
      // Redux Toolkit allows us to write "mutating" logic in reducers. It
      // doesn't actually mutate the state because it uses the Immer library,
      // which detects changes to a "draft state" and produces a brand new
      // immutable state based off those changes
      if(state.products.length>0){
        state.women = state.products.filter( pd=> pd.for === "female" && pd.category === state.womenSelectedCategory)
      }
    },
    setCartPrice:(state)=>{
      let totalPrice:number = 0;
      state.cart.forEach( pd =>{
        totalPrice += pd.price * pd.quantity;
      })
      state.cartPrice = totalPrice;
    },
    setMenProduct: (state) => {
      // Redux Toolkit allows us to write "mutating" logic in reducers. It
      // doesn't actually mutate the state because it uses the Immer library,
      // which detects changes to a "draft state" and produces a brand new
      // immutable state based off those changes
      if(state.products.length>0){
        state.men = state.products.filter( pd=> pd.for === "male" && pd.category === state.menSelectedCategory)
      }
    },
    setCartProduct: (state,action:{type:string, payload: ICart})=>{
        if(!state.cart.find( p=> p._id === action.payload._id)){
          const newPD:ICart = {...action.payload}
          state.cart.push(newPD)
        }else{
          const newCart = state.cart.find( pd=> pd._id === action.payload._id)
          if( newCart ){
            newCart.quantity = action.payload.quantity
            newCart.size = action.payload.size
          }
          // state.cart = [...newCart, action.payload]
        }
    },
    removeCartProduct:( state, action: {payload: string})=>{
      state.cart = state.cart.filter( pd => pd._id !== action.payload)
    },
    setAllProducts: (state,action:{type:string, payload: IProduct[]})=>{
        state.products = action.payload;
    },
    setNavHeight: (state,action:{ payload: number})=>{
        state.navHeight = action.payload;
    },
    setFilter: (state,action:{ payload: ISearch})=>{
        state.filter = action.payload;
    },
    setSingleProduct: (state,action:{type:string, payload: IProduct})=>{
          state.singleProduct = action.payload;
    },
    setCategoryBasedProduct: (state,action:{type:string, payload: IProduct[]})=>{
          state.categoryBasedProducts = action.payload;
    },
    setStatus: (state,action:{type:string, payload: TStatus})=>{
        state.status = action.payload;
    },
    
  },
  // The `extraReducers` field lets the slice handle actions defined elsewhere,
  // including actions generated by createAsyncThunk or in other slices.
});

export const { setWomenProduct, setWomenSelectedCategory, setCartProduct, setMenSelectedCategory, setMenProduct, setAllProducts, setStatus, setSingleProduct, setCategoryBasedProduct, setCartPrice, removeCartProduct, setFilter, setNavHeight} = storeSlice.actions;

export default storeSlice.reducer;
